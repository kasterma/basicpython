import heapq
import time
from collections import namedtuple, deque
from typing import List

snake = namedtuple("snake", ['tailx', 'taily', 'headx', 'heady'])


class Solution:
    def _moveright(self, s):
        return snake(tailx=s.tailx + 1, taily=s.taily, headx=s.headx + 1, heady=s.heady)

    def _can_right(self, s, grid):
        try:
            return grid[s.heady][s.headx + 1] == grid[s.taily][s.tailx + 1] == 0
        except IndexError:
            return False

    def _movedown(self, s):
        return snake(tailx=s.tailx, taily=s.taily + 1, headx=s.headx, heady=s.heady + 1)

    def _can_down(self, s, grid):
        try:
            return grid[s.heady + 1][s.headx] == grid[s.taily + 1][s.tailx] == 0
        except IndexError:
            return False

    def _rotate(self, s):
        return snake(tailx=s.tailx, taily=s.taily, headx=s.tailx, heady=s.taily + 1)

    def _can_rotate(self, s, grid):
        try:
            return s.headx > s.tailx and grid[s.heady + 1][s.headx] == grid[s.taily + 1][s.tailx] == 0
        except IndexError:
            return False

    def _rotate_anti(self, s):
        return snake(tailx=s.tailx, taily=s.taily, headx=s.tailx + 1, heady=s.taily)

    def _can_rotate_anti(self, s, grid):
        try:
            return s.heady > s.taily and grid[s.heady][s.headx + 1] == grid[s.taily][s.tailx + 1] == 0
        except IndexError:
            return False

    def _heuristic_to_go(self, s, n):
        """heuristic distance to goal steps for head + rotation if not horizontal"""
        return (n - s.headx) + (n - s.heady) + (1 if s.heady > s.taily else 0)

    def _solved(self, s, n):
        return s.tailx == n - 2 and s.heady == s.headx == s.taily == n - 1

    def minimumMoves(self, grid: List[List[int]]) -> int:
        n = len(grid)  # Note: grid is square
        s = snake(tailx=0, taily=0, heady=0, headx=1)
        start_pos = (0, s)
        seen = set(start_pos)
        h = deque(maxlen=n*n)
        h.append(start_pos)
        while h:
            steps, s = h.popleft()  # pop from beginning (*)
            if self._solved(s, n):
                return steps

            steps += 1

            def push_new(new_s):
                new_item = (steps, new_s)
                if not new_s in seen:
                    h.append(new_item)  # add at end (with (*) above ensures that first solution found is shortest)
                    seen.add(new_s)

            if self._can_down(s, grid): push_new(self._movedown(s))
            if self._can_right(s, grid): push_new(self._moveright(s))
            if self._can_rotate(s, grid): push_new(self._rotate(s))
            if self._can_rotate_anti(s, grid): push_new(self._rotate_anti(s))

        return -1

    def minimumMoves_heap(self, grid: List[List[int]]) -> int:
        n = len(grid)  # Note: grid is square
        s = snake(tailx=0, taily=0, heady=0, headx=1)
        h = []
        seen = set()
        heapq.heappush(h, (self._heuristic_to_go(s, n), 0, s, False))
        i = 0
        while h:
            _, steps, s, last_rotate = heapq.heappop(h)
            if self._solved(s, n):
                return steps
            steps += 1

            def push_new(new_s, rot):
                new_item = (steps + self._heuristic_to_go(new_s, n), steps, new_s, rot)
                if not new_s in seen:
                    heapq.heappush(h, new_item)
                    seen.add(new_s)

            if self._can_down(s, grid):
                push_new(self._movedown(s), False)
            if self._can_right(s, grid):
                push_new(self._moveright(s), False)
            if not last_rotate and self._can_rotate(s, grid):
                push_new(self._rotate(s), True)
            if not last_rotate and self._can_rotate_anti(s, grid):
                push_new(self._rotate_anti(s), True)
        else:
            return -1


